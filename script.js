    // Smooth Scrolling for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;

            const targetElement = document.querySelector(targetId);

            if (targetElement) {
                e.preventDefault();
                const headerOffset = 80;
                const elementPosition = targetElement.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.pageYOffset - headerOffset;

                window.scrollTo({
                    top: offsetPosition,
                    behavior: "smooth"
                });
            }
        });
    });

    // Language Switcher Logic --------------------------
    let currentLang = localStorage.getItem("lang") || "ar";

    function setLanguage(lang) {
        currentLang = lang;
        localStorage.setItem("lang", lang);

        // Update document dir and lang
        if (lang === "ar") {
            document.documentElement.setAttribute("dir", "rtl");
            document.documentElement.setAttribute("lang", "ar");
        } else {
            document.documentElement.setAttribute("dir", "ltr");
            document.documentElement.setAttribute("lang", "en");
        }

        // Translate elements with data-i18n
        document.querySelectorAll("[data-i18n]").forEach(element => {
            const key = element.getAttribute("data-i18n");
            if (translations[lang] && translations[lang][key]) {
                // If it's an input with placeholder, translate placeholder instead
                if (element.tagName === "INPUT" && element.hasAttribute("placeholder")) {
                    element.setAttribute("placeholder", translations[lang][key]);
                } else if (element.innerHTML.includes("<")) {
                    // Update innerHTML if contains tags
                    element.innerHTML = translations[lang][key];
                } else {
                    element.innerHTML = translations[lang][key];
                }
            }
        });

        // Update language switcher button text
        const langBtns = document.querySelectorAll('.lang-switcher');
        langBtns.forEach(btn => {
            btn.textContent = lang === "ar" ? "English" : "عربي";
        });
    }

    // Attach event listeners to lang switchers
    const langBtns = document.querySelectorAll('.lang-switcher');
    langBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            const newLang = currentLang === "ar" ? "en" : "ar";
            setLanguage(newLang);
        });
    });

